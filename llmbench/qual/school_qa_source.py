ELEMENTARY_SUBJECTS = ["國文", "數學", "自然", "社會", "英語"]
MIDDLE_SCHOOL_SUBJECTS = ["國文", "英語", "數學", "理化", "生物", "歷史", "地理", "公民"]

_LEVEL_MAP = {
    "elementary": "國小",
    "middle_school": "國中",
    "high_school": "高中",
    "both": "國小/國中",
}


# ---------------------------------------------------------------------------
# Public loader
# ---------------------------------------------------------------------------


def load_school_qa_materials(
    level: str = "both",
    subjects: Optional[List[str]] = None,
    data_jsonl: Optional[str] = None,
    limit: Optional[int] = None,
) -> List[RawMaterial]:
    """Load curriculum materials for school QA benchmark generation.

    Parameters
    ----------
    level:
        Which school level to include: ``"elementary"``, ``"middle_school"``, or ``"both"``.
    subjects:
        List of subject names to include (e.g. ``["數學", "理化"]``).
        ``None`` means all subjects for the selected level.
    data_jsonl:
        Optional path to a custom JSONL file.  Each line must be a JSON object
        with at least ``level``, ``subject``, ``title``, and ``content`` fields.
        When provided, built-in materials are *not* included.
    limit:
        Maximum number of materials to return. ``None`` means no limit.

    Returns
    -------
    list[RawMaterial]
    """
    if data_jsonl:
        raw_entries = _load_from_jsonl(data_jsonl)
    else:
        raw_entries = list(_BUILT_IN_MATERIALS)

    # Filter by level
    if level != "both":
        raw_entries = [e for e in raw_entries if e.get("level") == level]

    # Filter by subjects
    if subjects:
        subject_set = {s.strip() for s in subjects}
        raw_entries = [e for e in raw_entries if e.get("subject", "") in subject_set]

    if limit:
        raw_entries = raw_entries[:limit]

    materials: List[RawMaterial] = []
    for entry in raw_entries:
        entry_level = entry.get("level", "")
        entry_subject = entry.get("subject", "")
        title = entry.get("title", "")
        content = entry.get("content", "").strip()

        if not content or not title:
            logger.debug("school_qa: skipping entry with empty title or content")
            continue

        level_label = _LEVEL_MAP.get(entry_level, entry_level)

        materials.append(RawMaterial(
            source_category=f"school_qa/{entry_level}/{entry_subject}",
            title=f"【{level_label}·{entry_subject}】{title}",
            content=content,
            keyword=entry_subject,
            month_range={"start": "", "end": ""},
        ))

    logger.info(
        "school_qa: loaded %d materials (level=%s, subjects=%s)",
        len(materials), level, subjects,
    )
    return materials


def _load_from_jsonl(path: str) -> list[dict]:
    """Load entries from a JSONL file."""
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"school_qa: JSONL file not found: {p}")

    entries: list[dict] = []
    for lineno, line in enumerate(p.read_text(encoding="utf-8").splitlines(), 1):
        line = line.strip()
        if not line:
            continue
        try:
            entries.append(json.loads(line))
        except json.JSONDecodeError as exc:
            logger.warning("school_qa: skipping invalid JSON on line %d: %s", lineno, exc)
    return entries
